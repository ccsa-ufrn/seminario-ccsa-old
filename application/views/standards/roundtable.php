<h1>NORMA DOS MESAS-REDONDAS - XX SEMINÁRIO DE PESQUISA DO CCSA</h1>

<h2>NORMAS PARA MESAS-REDONDAS</h2>

<p>A proposta de Mesa-Redonda devera ser encaminhada para a Comissão Organizadora do Seminário, no período de 26 de janeiro a 08 de março de 2015, contendo: Titulo da mesa-redonda; Turno preferencial para realizar a Mesa Redonda; Objetivos; Nome do Coordenador; Nome do Departamento; Nome dos participantes da mesa-redonda e a vinculação institucional.</p>
<p>A relação das mesas-redondas aceitas para o Seminário será divulgada a partir do dia 23 de março de 2015 na página <seminario.ccsa.ufrn.br>.</p>

<h2>DAS MESAS-REDONDAS</h2>
<p>Os Certificados dos coordenadores e debatedores serão entregues a partir do dia <b>15 de junho de 2015</b>, mediante entrega da lista de presença pelo Coordenador da Mesa, na Secretaria do CCSA e ficarão disponíveis <b>até 90 (noventa) dias</b> após o evento (14 de setembro de 2015). Após esta data a não serão fornecidos certificados.</p>

<h2>DO VALOR DA INSCRIÇÃO</h2>
<p>Para inscrição no Seminário:</p>
<p><b>Alunos: R$ 20,00 (vinte Reais).</b></p>
<p><b>Professores/Técnicos: R$ 40,00 (quarenta Reais)</b></p>
<p>Os participantes que desejam ter os seus trabalhos publicados nos anais do evento devem realizar o pagamento até o dia <b>10 de abril de 2015</b>. Para aqueles que desejam apenas participar dos minicursos, o pagamento pode ser feito até o dia <b>27 de abril de
2015</b>. As datas indicadas acima são, também, em cada caso, os limites para envio do comprovante de pagamento pelo sistema.</p>
<p>Obs.: a inscrição vale para o Seminário como um todo. Logo, com ela o participante tem acesso a todas as atividades.</p>

<h2>DA FORMA DA INSCRIÇÃO E DO PAGAMENTO</h2>
<p>Conforme informações disponibilizadas na página do Seminário.</p>

<h2>DA ENTREGA DO MATERIAL</h2>
<p>O material do evento será entregue apenas para aqueles que apresentarem o comprovante de inscrição. 
Obs.: o local da entrega será divulgado oportunamente.</p>
