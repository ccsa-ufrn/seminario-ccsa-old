<h2>FORMATO DA APRESENTAÇÃO DOS ARTIGOS NOS GRUPOS TEMÁTICOS</h2>
<p>A comunicação oral será feita no tempo de 15 (quinze) a 20 (vinte) minutos de exposição para os trabalhos técnico-científicos, dando prioridade ao problema da pesquisa, à metodologia e aos resultados obtidos.</p>

<h2>SUBMISSÃO DOS ARTIGOS PARA OS GRUPOS TEMÁTICOS</h2>
<p>A submissão dos artigos será online, em formato PDF.</p>

<h2>INFORMAÇÕES SOBRE ACEITAÇÃO DOS TRABALHOS</h2>
<p>A Comissão Científica do XX Seminário de Pesquisa do CCSA encaminhará até o dia 30 de março de 2015, comunicado de aceitação ou não dos trabalhos (artigos para comunicação oral ou pôsteres) para os respectivos autores. Informará também os trabalhos na página <seminario.ccsa.ufrn.br>.</p>
<p>Os autores de trabalhos selecionados devem confirmar sua participação mediante inscrição no XX Seminário de Pesquisa do CCSA até 10 de abril de 2015, para que os mesmos possam ser incluídos na programação oficial e nos Anais, conforme instruções de inscrição.
Apenas os trabalhos (artigos e pôsteres) que forem apresentados no Seminário constarão dos Anais.</p>
<p>Os artigos apresentados para comunicação oral nos GTs poderão ser redirecionados pelo avaliador, para apresentação na sessão de pôsteres. Esta comunicação será feita na página <seminário.ccsa.ufrn.br> até o dia 30 de março de 2015. Caso o(s) autor(es) não queira(m) apresentar o trabalho nessa modalidade, deve(m) informar à Comissão organizadora, pessoalmente ou através do e-mail <seminariodepesquisa@ccsa.ufrn.br> até o dia 10 de abril de 2015. Após essa data, a Comissão entenderá que a sugestão de apresentação do artigo na forma de pôster foi aceita. Os autores de pôsteres devem observar a data limite para confirmar a sua participação: 10 de abril de 2015.</p>

