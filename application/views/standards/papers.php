<h1>NORMA DOS ARTIGOS - XX SEMINÁRIO DE PESQUISA DO CCSA</h1>

<h2>INFORMAÇÕES GERAIS PARA A INSCRIÇÃO DOS ARTIGOS E PÔSTERES NOS GRUPOS TEMÁTICOS (GTs)</h2>

<p>Quando o trabalho (artigo ou pôster) tiver mais de um autor, pelo menos um deve se inscrever no Seminário. Cada artigo ou pôster poderá ser apresentado por no máximo 05 (cinco) pessoas.</p>
<p>A Comissão Cientifica julgará os trabalhos exclusivamente por sua qualidade e, somente aqueles que cumprirem as normas de formatação, redação, ortografia e gramática, serão encaminhados para avaliação. Não serão permitidas substituições de artigos.
<p>A autoria do artigo deve ser feita apenas na página do resumo.</p>
<p>Antes de enviar o trabalho (pôster/artigo), certifique-se de que os arquivos não estejam infectados com vírus, examinando-os cuidadosamente. Trabalhos que não atendam a essa exigência serão excluídos.</p>
<p>Os trabalhos (pôster/artigo) devem ser entregues no período de 26/01/2015 até 08/03/2015.</p>

<h2>NORMAS DE PADRONIZAÇÃO PARA SEREM OBSERVADAS NA ELABORAÇÃO DOS TRABALHOS</h2>
<ul>
    <li>NBR 6022 Informação e documentação: artigo em publicação periódica científica impressa. Apresentação, maio 2003;</li>
    <li>NBR 6023 Informação e documentação: elaboração de referências, agosto 2002;</li>
    <li>NBR 6028 Resumo (Tipo Informativo), novembro de 2003;</li>
    <li>NBR 10520 Informação e documentação: citações em documento. Apresentação, agosto 2002.</li>
</ul>

<h2>FORMATAÇÃO PARA TRABALHOS NA FORMA DE ARTIGOS</h2>
<ul>
    <li>Os trabalhos técnico-científicos deverão conter de 10 a 12 páginas, incluindo anexos, tabelas e gráficos.  Não serão aceitas notas de rodapé. Todas deverão estar incluídas com nota de final de texto.</li>
    <li>Na primeira página deve constar o grupo temático escolhido, o título em letras maiúsculas, o nome de um ou mais autores, o nome e endereço da instituição de origem, incluindo país e respectivos endereços eletrônicos.</li>
    <li>Papel tamanho A-4. Utilizar versão 6.0 ou posterior do editor de texto Word for Windows, fonte Arial, corpo 12.</li>
    <li>Tabelas e gráficos na mesma versão (fonte Arial, corpo 11).</li>
    <li>Entrelinha e espaçamento de 1,5cm.</li>
    <li>Margens superior e esquerda de 3cm; margens inferior e direita de 2,0cm.</li>
    <li>O artigo deverá ser acompanhado de um resumo. Este deve conter de 200 a 500 palavras a contar do titulo e deve ser um texto corrido, sem parágrafos, em folha separada do corpo do trabalho, mas com a identificação dos autores. O resumo deve ser em espaço simples.  Indicar, ao final do resumo, o apoio financeiro recebido para a pesquisa, se houver.</li>
